void	*ft_memchr(const void *s, int c, size_t n)
{
	int		i;
	char	*str;

	i = 0;
	str = s;
	while (i < n)
	{
		if (str[i] == c)
			return (str[i]);
		else
			i++;
	}
	return (NULL);
}					
