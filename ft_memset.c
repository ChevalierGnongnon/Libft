void	*ft_memset(void *ptr, int val, size_t count)
{
	size_t	i;
	char	str;

	i = 0;
	str = ptr;
	while (i < cnt && str[i] != '\0')
	{
		str[i] = val;
		i++;
	}
	return (str);
}
